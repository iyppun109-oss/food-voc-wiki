export function getReportTemplate(vocType: string): ReportTemplate {
  const normalized = normalizeForTemplateMatch(vocType)
  const matched = TEMPLATE_ALIASES.find((entry) =>
    entry.keywords.some((keyword) => normalized.includes(normalizeForTemplateMatch(keyword))),
  )

  return matched ? REPORT_TEMPLATES[matched.type] : DEFAULT_REPORT_TEMPLATE
}
