function normalizeForTemplateMatch(value: string) {
  return value.trim().toLowerCase()
}

export function getReportSentenceTemplate(
  vocType: string,
): ReportSentenceTemplate {
  const normalized = normalizeForTemplateMatch(vocType)
  const matched = REPORT_TEMPLATE_ALIASES.find((entry) =>
    entry.keywords.some((keyword) => normalized.includes(normalizeForTemplateMatch(keyword))),
  )

  return matched ? REPORT_SENTENCE_TEMPLATES[matched.type] : DEFAULT_REPORT_SENTENCE_TEMPLATE
}
